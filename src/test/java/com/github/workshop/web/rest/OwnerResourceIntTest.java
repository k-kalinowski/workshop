package com.github.workshop.web.rest;

import com.github.workshop.WorkshopApp;

import com.github.workshop.domain.Owner;
import com.github.workshop.repository.OwnerRepository;
import com.github.workshop.service.OwnerService;
import com.github.workshop.service.dto.OwnerDTO;
import com.github.workshop.service.mapper.OwnerMapper;
import com.github.workshop.web.rest.errors.ExceptionTranslator;

import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.mockito.MockitoAnnotations;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.data.web.PageableHandlerMethodArgumentResolver;
